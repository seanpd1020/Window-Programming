                var jperson = JsonConvert.DeserializeObject<dynamic>(jsonstr);
                JArray items = (JArray)jperson;
                int length = items.Count;
                Global.data = new Global.Data[length];
                int question_index = 0;
                Global.question = new string[1];
                Global.question[question_index] = "頭痛";
                for (int i = 0; i < length; i++)
                {
                    string x = jperson[i].acupuncture.ToString();
                    Global.data[i].acupuncture = x;
                    string[] temp = jperson[i].feature.ToString().Split(' ');
                    
                    int k = 0;
                    Global.data[i].feature = new string[1];
                    
                    foreach (string tmp in temp)
                    {
                        Global.data[i].feature[k++] = tmp;
                        Array.Resize(ref Global.data[i].feature, Global.data[i].feature.Length + 1);
                        int check = 0;
                        if(Global.question!=null)
                            foreach (string tar in Global.question)
                            {
                                if (tmp.Equals(tar))
                                   check = 1;
                            }
                            if(check == 0)
                            {
                                Global.question[question_index++] = tmp;
                                Array.Resize(ref Global.question, Global.question.Length + 1);
                            }
                    }
                    Array.Resize(ref Global.data[i].feature, Global.data[i].feature.Length - 1);
                    //string[] tmpfeature = termsList.ToArray();
                    //textBox1.AppendText(jperson[i].acupuncture.ToString() + "\n");
                    //textBox1.AppendText(jperson[i].feature.ToString() + "\n");
                }
            }
            catch (Exception ex)
            {
                textBox1.AppendText(ex.Message.ToString());
            }
            foreach(var d in Global.data)
            {
                textBox1.AppendText(d.acupuncture + "\n");
                foreach(string s in d.feature)
                {
                    textBox1.AppendText(s + " ");
                }
                textBox1.AppendText("\n");
            }
